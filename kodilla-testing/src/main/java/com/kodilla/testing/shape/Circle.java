package com.kodilla.testing.shape;

public class Circle implements Shape {
    private double field;
    private String name;
    private double pi;
    private double r;


    public Circle(String name) {
        this.name = name;

    }

    public String getShapeName(){
        return name = "Circle";
    }
    public double getField(){
        return field =  pi * (r * r);
    }
}

